<script>
import axios from "axios";
import {
  validationMixin
} from 'vuelidate'
import {
  required,
  url

} from 'vuelidate/lib/validators'




export default {
  name: "add-product",
  mixins: [validationMixin],
  data() {
    return {
      product: {},
      selectedFile: null,
      submitted: false,
      semesters: ["1", "2", "3", "4", "5", "6", "7", "8"],
      branches: [
        "CSE",
        "ISE",
        "ECE",
        "MECH",
        "CIV",
        "BIO",
        "MED",
        "ELEC",
        "ARCH"
      ]
    };
  },
  validations: {
    product: {
      title: {
        required

      },
      author: {
        required,
        alphabet(author){
        return(
        /^[a-zA-Z ]*$/.test(author)
        );
        }


      },
      publisher: {
        required
      },

      branch: {
        required
      },
      sem: {
        required
      },
      link: {
        required,
        url
      },
      description: {
        required
      },
      donation: {
        required
      },
      location: {
        required
      },
      phone: {
        required,
        number(phone){
        return(
        /^(\+91( )?)?[0-9]{10}$/.test(phone)
        );
        }

      }

    }
  },
  methods: {
    getValidationClass(fieldName) {
      const field = this.$v.product[fieldName]

      if (field) {
        return {
          'md-invalid': field.$invalid && field.$dirty
        }
      }
    },
    onFileSelected(event) {
      console.log("set file");
      console.log(event);
      this.selectedFile = event.target.files[0];
    },
    validateBook() {
      this.$v.product.title.$touch();
      this.$v.product.author.$touch();
      this.$v.product.publisher.$touch();
      this.$v.product.sem.$touch();
      this.$v.product.branch.$touch();
      this.$v.product.donation.$touch();
      this.$v.product.phone.$touch();

      console.log(this.$v.$invalid)

      if (!this.$v.product.title.$invalid || !this.$v.product.author.$invalid || !this.$v.product.publisher.$invalid || !this.$v.product.sem.$invalid || !this.$v.product.branch.$invalid || !this.$v.product.donation.$invalid || !this.$v.product.phone.$invalid ) {
        this.saveBook()
      }
    },

    validateLink() {
      this.$v.product.title.$touch();
      this.$v.product.link.$touch();
      this.$v.product.description.$touch();
      this.$v.product.sem.$touch();
      this.$v.product.branch.$touch();
      console.log(this.$v.$invalid)

      if (!this.$v.product.title.$invalid || !this.$v.product.link.$invalid || !this.$v.product.description.$invalid || !this.$v.product.sem.$invalid || !this.$v.product.branch.$invalid) {
        this.saveDrive()
      }
    },

    validateOther() {
      this.$v.product.title.$touch();
      this.$v.product.description.$touch();
      this.$v.product.sem.$touch();
      this.$v.product.branch.$touch();
      this.$v.product.donation.$touch();
      this.$v.product.phone.$touch();

      console.log(this.$v.$invalid)

      if (!this.$v.product.title.$invalid || !this.$v.product.description.$invalid || !this.$v.product.sem.$invalid || !this.$v.product.branch.$invalid || !this.$v.product.donation.$invalid || !this.$v.product.phone.$invalid) {
        this.saveOther()
      }
    },

    validateLost() {
      this.$v.product.title.$touch();
      this.$v.product.description.$touch();
      this.$v.product.location.$touch();
      this.$v.product.phone.$touch();
      console.log(this.$v.$invalid)

      if (!this.$v.product.title.$invalid || !this.$v.product.description.$invalid || !this.$v.product.location.$invalid || !this.$v.product.phone.$invalid) {
        this.saveLostfound()
      }
    },

    saveBook() {

      const fd = new FormData();
      // image stuff
      fd.append("image", this.selectedFile, this.selectedFile.name);
      // other data
      fd.append("title", this.product.title);
      fd.append("author", this.product.author);
      fd.append("sem", this.product.sem);
      fd.append("branch", this.product.branch);
      fd.append("publisher", this.product.publisher);
      fd.append("donation", this.product.donation);
      fd.append("phone", this.product.phone);
      fd.append("userId", this.$store.state.userId);

      try {
        axios
          .post("http://localhost:8080/api/things/upload-book", fd, {
            headers: {
              "content-type": undefined,
              Authorization: "Bearer " + this.$store.state.token
            }
          })
          .then(response => {
            this.book.id = response.data.id;
          })
          .catch(e => {
            console.log(e);
          });
      } catch (err) {
        console.log(err);
      }

      this.submitted = true;
      this.$router.push("/");
      location.reload();
    },

    saveDrive() {


      const fd = new FormData();

      fd.append("title", this.product.title);
      fd.append("sem", this.product.sem);
      fd.append("branch", this.product.branch);
      fd.append("url", this.product.link);
      fd.append("description", this.product.description);
      fd.append("userId", this.$store.state.userId);
      try {
        axios
          .post("http://localhost:8080/api/things/upload-drive", fd, {
            headers: {
              "content-type": undefined,
              Authorization: "Bearer " + this.$store.state.token
            }
          })
          .then(response => {

            this.book.id = response.data.id;
          })
          .catch(e => {

            console.log(e);
          });
      } catch (err) {
        console.log(err);
      }
      this.submitted = true;
      this.$router.push("/");
      location.reload();

    },
    saveOther() {


      const fd = new FormData();
      // image stuff
      fd.append("image", this.selectedFile, this.selectedFile.name);
      // other data
      fd.append("title", this.product.title);
      fd.append("sem", this.product.sem);
      fd.append("branch", this.product.branch);
      fd.append("description", this.product.description);
      fd.append("donation", this.product.donation);
      fd.append("phone", this.product.phone);
      fd.append("userId", this.$store.state.userId);
      try {
        axios
          .post("http://localhost:8080/api/things/upload-other", fd, {
            headers: {
              "content-type": undefined,
              Authorization: "Bearer " + this.$store.state.token
            }
          })
          .then(response => {
            this.book.id = response.data.id;
          })
          .catch(e => {
            console.log(e);
          });
      } catch (err) {
        console.log(err);
      }
      this.submitted = true;
      this.$router.push("/");
      location.reload();

    },
    saveLostfound() {


          const fd = new FormData();
          // image stuff
          fd.append("image", this.selectedFile, this.selectedFile.name);
          // other data
          fd.append("title", this.product.title);
          fd.append("description", this.product.description);
          fd.append("location", this.product.location);
          fd.append("phone", this.product.phone);
          fd.append("userId", this.$store.state.userId);
          try {
             axios
              .post("http://localhost:8080/api/lostfound/post", fd, {
                headers: {
                  "content-type": undefined,
                  Authorization: "Bearer " + this.$store.state.token
                }
              })
              .then(response => {
                this.lostfound.id = response.data.id;
              })
              .catch(e => {
                console.log(e);
              });
          } catch (err) {
            console.log(err);
          }
          this.submitted = true;
          this.$router.push("/");
          location.reload();

    },

    newProduct() {
      this.submitted = false;
      this.product = {};
    }
  }
};
</script>

